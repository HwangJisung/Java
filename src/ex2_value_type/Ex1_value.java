package ex2_value_type;

public class Ex1_value {
	public static void main(String[] args) {
		// 기본자료형(valueType)
		// 논리형 : boolean
		// 문자형 : char  
		// 정수형 : byte(1), short(2), int(4), long(8) 
        // 실수형 : float(4), double(8)
		int a = 100; 
		int b = 200;
		
		int c = a+b;
		System.out.println(c);
			
	}
}
